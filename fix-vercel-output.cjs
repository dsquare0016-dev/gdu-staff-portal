const fs = require('fs');
const path = require('path');

console.log('Current working directory:', process.cwd());
const distDir = path.join(process.cwd(), 'dist');
const vercelOutputDir = path.join(process.cwd(), '.vercel', 'output');
const vercelStatic = path.join(vercelOutputDir, 'static');
const vercelFunctions = path.join(vercelOutputDir, 'functions', 'index.func');

console.log('Ensuring Vercel Build Output is correctly placed...');

if (!fs.existsSync(distDir)) {
  console.error('Error: dist directory not found. Did the build run?');
  process.exit(1);
}

// Ensure directories exist
[vercelOutputDir, vercelStatic, path.join(vercelOutputDir, 'functions'), vercelFunctions].forEach(dir => {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
});

function copyRecursiveSync(src, dest) {
  const exists = fs.existsSync(src);
  const stats = exists && fs.statSync(src);
  const isDirectory = exists && stats.isDirectory();
  if (isDirectory) {
    if (!fs.existsSync(dest)) {
      fs.mkdirSync(dest, { recursive: true });
    }
    fs.readdirSync(src).forEach((childItemName) => {
      copyRecursiveSync(path.join(src, childItemName), path.join(dest, childItemName));
    });
  } else {
    fs.copyFileSync(src, dest);
  }
}

try {
  // 1. Copy static assets from dist/client
  const clientDist = path.join(distDir, 'client');
  if (fs.existsSync(clientDist)) {
    copyRecursiveSync(clientDist, vercelStatic);
    console.log('Successfully copied assets from dist/client to .vercel/output/static');
  }

  // 2. Copy config.json from dist
  const configJson = path.join(distDir, 'config.json');
  if (fs.existsSync(configJson)) {
    fs.copyFileSync(configJson, path.join(vercelOutputDir, 'config.json'));
    console.log('Successfully copied config.json to .vercel/output/config.json');
    
    // Verify the copied config.json is valid JSON
    const content = fs.readFileSync(path.join(vercelOutputDir, 'config.json'), 'utf8');
    try {
      JSON.parse(content);
      console.log('Verified .vercel/output/config.json is valid JSON');
    } catch (e) {
      console.error('Error: Copied config.json is NOT valid JSON!');
      process.exit(1);
    }
  } else {
    console.warn('Warning: dist/config.json not found. Vercel deployment might fail if not generated by Nitro.');
  }

  // 3. Copy server functions from dist/server to functions/index.func
  const serverDist = path.join(distDir, 'server');
  if (fs.existsSync(serverDist)) {
    copyRecursiveSync(serverDist, vercelFunctions);
    console.log('Successfully copied server functions to .vercel/output/functions/index.func');
  }

  // 4. Merge public folder into static
  const publicDir = path.join(process.cwd(), 'public');
  if (fs.existsSync(publicDir)) {
    copyRecursiveSync(publicDir, vercelStatic);
    console.log('Successfully merged public folder into .vercel/output/static');
  }
  
  console.log('Vercel Build Output preparation complete.');
} catch (err) {
  console.error('Error preparing Vercel output:', err);
  process.exit(1);
}
